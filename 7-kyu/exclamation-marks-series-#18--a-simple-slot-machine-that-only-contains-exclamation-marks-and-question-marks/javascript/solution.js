const slot = s => {
  const lookup = [
    [/^!{5}$/, 1000],
    [/^\?{5}$/, 1000],
    [/!{4}/, 800],
    [/\?{4}/, 800],
    [/^!{3}\?{2}$|^\?{2}!{3}$|^\?{3}!{2}$|^!{2}\?{3}$/, 500],
    [/!{3}/, 300],
    [/\?{3}/, 300],
    [/\?\?!!|!!\?\?|^!{2}\?!{2}$|^\?{2}!\?{2}$/, 200],
    [/\?\?|!!/g, 100]
  ];
  for (const [k, v] of lookup) {
    if (k.test(s)) {
      return v;
    }
  }
  return 0;
};
